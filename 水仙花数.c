//打印出所有的“水仙花数”，所谓“水仙花数”是指一个三位数，
//其各位数字立方和等于该数本身。
//例如：153是一个“水仙花数”，
//因为153=1的三次方＋5的三次方＋3的三次方。

#include "stdio.h"
int main() {
  int n;
  int a, b, c;
  for (n = 100; n <= 999; n++) {
    a = n / 100;
    b = (n - 100 * a) / 10;
    c = n - 100 * a - 10 * b;

    if (a * a * a + b * b * b + c * c * c == n)
      printf("%d是水仙花数\n", n);
  }
}
