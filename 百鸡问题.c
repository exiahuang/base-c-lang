//公元钱五世纪，我国古代数学家张丘建
//在《算经》一书中提出了“百鸡问题”：
//鸡翁一值钱五，鸡母一值钱三，鸡雏三值钱一。
//百钱买百鸡，问鸡翁、鸡母、鸡雏各几何？

#include "stdio.h"
int main() {
  int a, b, c;
  for (a = 0; a <= 100; a++)
    for (b = 0; b <= 100; b++)
      for (c = 0; c <= 100; c++)
        if ((5 * a + 3 * b + c / 3 == 100) && (a + b + c == 100))
          printf("鸡翁%d只\n鸡母%d只\n鸡雏%d只\n\n", a, b, c);
}